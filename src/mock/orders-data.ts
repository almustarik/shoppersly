export type OrderStatus =
  | "pending"
  | "confirmed"
  | "packed"
  | "shipped"
  | "delivered"
  | "returned"
  | "cancelled"

export type PaymentMethod = "cod" | "bkash" | "nagad" | "bank_transfer"
export type PaymentStatus = "unpaid" | "paid" | "partial" | "refunded"
export type Courier = "pathao" | "steadfast" | "redx" | "paperfly"

export interface OrderItem {
  id: string
  name: string
  variant?: string
  qty: number
  price: number
}

export interface OrderTimelineEntry {
  status: OrderStatus | "note"
  label: string
  timestamp: string
  actor?: string
}

export interface Order {
  id: string
  orderId: string
  customer: {
    name: string
    phone: string
    email?: string
    address: string
    city: string
    zone?: string
    orderCount: number
  }
  items: OrderItem[]
  subtotal: number
  deliveryCharge: number
  discount: number
  total: number
  status: OrderStatus
  paymentMethod: PaymentMethod
  paymentStatus: PaymentStatus
  courier?: Courier
  trackingId?: string
  timeline: OrderTimelineEntry[]
  notes: string[]
  source: "facebook" | "instagram" | "website" | "manual"
  createdAt: string
  updatedAt: string
}

